// -픽셀, dpi, dp단위
// -픽셀 -> 핸드폰 화면에서 빛이 나오는 전구 -> 가장 작은 단위
// -dpi -> dot per inch
//      - ldpi -> 120 (1인치에 120픽셀)
//      - mdpi -> 160
//      - hdpi -> 240
//      - xhdpi -> 320
//      - xxhdpi -> 480
//      - xxxhdip -> 640
// -dp -> desity independent
//      - 픽셀 독립적인 단위
